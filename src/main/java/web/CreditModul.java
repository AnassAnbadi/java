package web;

public class CreditModul {
	private double m;
	private double t;
	private int d;
	private double mons;

	public double getM() {
		return m;
	}
	public void setM(double m) {
		this.m = m;
	}
	public double getT() {
		return t;
	}
	public void setT(double t) {
		this.t = t;
	}
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	public double getMons() {
		return mons;
	}
	public void setMons(double mons) {
		this.mons = mons;
	}

}
